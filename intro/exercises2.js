// zwei Variablen
let zahl1 = 10;
let zahl2 = 5;

// Addition
let addition = zahl1 + zahl2;
console.log("Addition: " + addition); // Ausgabe: Addition: 15

// Subtraktion
let subtraktion = zahl1 - zahl2;
console.log("Subtraktion: " + subtraktion); // Ausgabe: Subtraktion: 5

// Multiplikation
let multiplikation = zahl1 * zahl2;
console.log("Multiplikation: " + multiplikation); // Ausgabe: Multiplikation: 50

// Division
let division = zahl1 / zahl2;
console.log("Division: " + division); // Ausgabe: Division: 2

// LÃ¤ngere Formel mit Klammern
let formel = (zahl1 + zahl2) * (zahl1 - zahl2);
console.log("Formel: " + formel); // Ausgabe: Formel: 75

// Hochzahl (Potenz)
let potenz = Math.pow(zahl1, 2); // zahl1 hoch 2 altern. zahl1 ** 2
console.log("Potenz: " + potenz); // Ausgabe: Potenz: 100

// Wurzelrechnung
let wurzel = Math.sqrt(zahl1); // Quadratwurzel von zahl1
console.log("Wurzel: " + wurzel); // Ausgabe: Wurzel: 3.1622776601683795


